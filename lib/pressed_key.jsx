class PressedKey {
    constructor(props) {
        this.val = props.val;
        if (this.val == "q") {
            this.pos = [];
        } else if (this.val == "w") {
            this.pos = [];
        } else if (this.val == "e") {
            this.pos = [];
        } else if (this.val == "r") {
            this.pos = [];
        } else if (this.val == "t") {
            this.pos = [];
        }
    }
}